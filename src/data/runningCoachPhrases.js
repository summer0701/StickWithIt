export function getCoachPhraseItems() {
  return Object.entries(runningCoachPhrases).flatMap(([category, phrases]) =>
    phrases.map((text, index) => ({
      key: `${category}_${String(index + 1).padStart(3, '0')}`,
      category,
      text,
    })),
  );
}
